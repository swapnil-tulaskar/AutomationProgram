package day11;

public class Positive_Negative_Zero_Number {

	public static void main(String[] args) {
		
		int ipValue = -5;
		
		if (ipValue>0) {
			System.out.println("Number is Positive");
		}
	  else if(ipValue<0) {
			System.out.println("Number is Negative");
		}	  else {
		  System.out.println("Number is Zero");
	  }

	}

}
